------------------------------------------------------------------------------
module Main where

------------------------------------------------------------------------------
-- | Source
import Maya


------------------------------------------------------------------------------
-- |
pSet :: PolySet
pSet =
    [ (cuboid, "a", ((5, 0, 0), (90, 0, 0), (1, 0.5, 1)))
    , (cuboid, "b", ((-5, 0, 0), (90, 0, 0), (1, 0.5, 1))) ]

------------------------------------------------------------------------------
-- | Write the commands to file.
main :: IO ()
main = do
    let title = "basic"
    let cmds = polySetToCmds pSet
    writeFile (title ++ ".py") $ (preamble title ++ cmds)
    return ()